package corejava.Stringmethods;

public class substring {
    public static void main(String[] args) {
        String ss= "bhuvi vasanth";
        System.out.println(ss.substring(4));
        System.out.println(ss.substring(0, 10));

        System.out.println(ss.toUpperCase());
        System.out.println(ss.toLowerCase());
        System.out.println(ss.trim());
        String a="arun prasath ";
        System.out.println(a+"yasodha ");
        System.out.println(a.trim()+"bhuvana ");

    }
}
